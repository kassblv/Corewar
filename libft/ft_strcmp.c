int		ft_strcmp(const char *s1, const char *s2)
{
	unsigned int	i;
	unsigned char	*c1;
	unsigned char	*c2;

	c1 = (unsigned char *)s1;
	c2 = (unsigned char *)s2;
	i = 0;
	while ((c1[i] == c2[i]) && c1[i] && c2[i])
		i++;
	return (c1[i] - c2[i]);
}
